package day03;

public class C81_AsciiTable {
    public static void main(String[] args) {
        int sayi = 10;
        char harf = 'd';
        String str = "banana";

        System.out.println(sayi + harf);
        System.out.println(sayi * harf);
        System.out.println(str + harf);
        System.out.println(sayi + "" + harf);


        int sayi1 = 10;
        int sayi2 = 20;
        sayi1 = 20;
        sayi2 = 10;
        System.out.println("sayi1 = " + sayi1);
        System.out.println("sayi2 = " + sayi2);
    }
}
